import Foundation
import UIKit
import Hex

extension UIColor {
    
    public static func redFavorite() -> UIColor {
        return UIColor(hex: "EB564F")
    }
    
    public static func grayNavy() -> UIColor {
        return UIColor(hex: "132D42")
    }
}
